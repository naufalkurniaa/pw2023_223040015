    <h1 class="text-start mb-3">Kisah Alek, Warga Bogor yang Merantau di Pasar Koja Demi Jual Amplop Lebaran</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023</p>
    <img src="img/gambar19.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Lima pria asal Leuwiliang, Bogor, bertolak ke Pasar Koja Baru, Minggu (16/4/2023). Mereka hendak berjualan amplop Lebaran di kawasan tersebut. Salah satu dari dari mereka adalah Alek (37). Dia mengungkapkan, sudah tujuh tahun terakhir melakoni pekerjaan musiman ini.</p>
    <p class="text-start"> Setelah kelimanya sampai, kelompok mereka menjadikan Pasar Koja Baru sebagai "rumah singgah" hingga gema takbir tiba. "Kami enggak mengontrak, masa cuma lima hari saja mengontrak. Ya kami tidur di sini (Pasar Koja Baru), seadanya," ucap Alek kepada Kompas.com pada Kamis (20/4/2023). </p>
    <p class="text-start"> "Pokoknya, ini ibaratnya kamar kami," lanjut dia sembari menyapu pandang ke penjuru pasar. Alek dan teman-temannya mengaku terbiasa merantau seperti ini. Oleh sebab itu, ia terbiasa dengan ketidaknyamanan hidup di area pasar tradisional yang identik jorok. </p>
    <p class="text-start"> "Biasa hidup kayak begini, kalau yang enggak biasa, mungkin enggak bakal kuat. Nyamuk banyak banget. Ibarat laler bertemu ikan asin," seloroh Alek. Lantas, mengapa Alek tidak berjualan amplop Lebaran di pasar Bogor saja? Mengingat jarak yang pastinya tidak terlalu jauh dari domisilinya.  Menurut Alek, ia ingin mencari tempat yang sepi pesaing. </p>
    <p class="text-start"> "(Enggak jualan di Bogor) di sana juga banyak (pedagang amplop Lebaran), banyak pesaing. Makanya, lebih ke kota dan di kota mayoritas masyarakatnya kerja," kata Alek. Alek kemudian menceritakan keseharian para pedagang amplop Lebaran. Setiap pukul 05.30 WIB, ia dan kawan-kawannya mulai berpencar dari Pasar Koja Baru untuk mencari pembeli di pasar pagi. </p>
    <p class="text-start"> Alek memilih Pasar Jalan Baru, Kali Baru, Jakarta Utara, untuk mencari sesuap nasi di sana. "Nanti Ashar, semuanya balik lagi ke sini (Pasar Koja Baru). Setelah maghrib, baru kami pindah lagi ke depan Jakarta Islamic Centre (JIC)," tutur Alek. </p>
    <p class="text-start"> "Nanti, sekitar 23.00 WIB, balik ke sini. Kalau ada satu dari kami yang hilang, ya kami cari," lanjut dia. Berapa pun hasilnya, Alek memastikan akan tetap pulang pada malam takbir untuk merayakan momen Lebaran bersama keluarga. </p>
    <p class="text-start"> "Iya (berapa pun penghasilannya). Intinya, kalau misalnya kami enggak punya uang untuk balik, ya bagaimana caranya bisa sampai Bogor, entah numpang dengan mobil bak terbuka," ujar Alek. </p>
    <p class="text-start"> Sayangnya, tahun ini tidak seberuntung tahun-tahun sebelumnya. Penjualan amplop Lebaran kali ini menurun.  "Kalau dagang sih enggak ada yang bisa menentukan. Jadi, dagang itu kalau lagi ramai, ya ramai. Cuma, kalau tahun sekarang itu, lagi susah-susahnya. Tahun ini doang, kalau tahun kemarin enggak," ungkap Alek. </p>
    <p class="text-start"> Lebaran tahun-tahun lalu, pembeli amplop Lebaran sudah ramai pada hari kedua atau ketiga mereka berjualan. Tetapi, sekarang sudah berhari-hari berjualan, hasilnya tetap mengecewakan.  "Ibaratnya, kami satu hari Rp 100.000. Paling sekarang Rp 30.000. Anjlok jauh banget, lebih dari 50 persen," ucap Alek yang kebingungan soal penyebab dagangannya kurang laku keras dibandingkan era Covid-19 masih mewabah di Tanah Air. </p>
    <p class="text-start"> Tentu hidup Alek dan kawan-kawan masih terus berjalan. Sekarang gagal, besok usaha lebih keras lagi. Begitu kata Alek yang pada Jumat sore ini akan pulang ke rumahnya di Bogor.  </p>
