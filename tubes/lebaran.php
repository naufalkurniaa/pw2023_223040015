    <h1 class="text-start mb-3">Lebaran, KSAL: Kapal Patroli Maritim Tidak Akan Dikurangi untuk Cegah Penyelundupan</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023</p>
    <img src="img/gambar18.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Kepala Staf TNI Angkatan Laut (KSAL) Laksamana Muhammad Ali menyatakan bahwa unsur-unsur kapal patroli tidak akan dikurangi pada saat libur Hari Raya Idul Fitri 1444 Hijriah. Ali mengatakan, hal itu dilakukan untuk mencegah terjadinya penyelundupan.</p>
    <p class="text-start"> Dalam rangka menyambut lebaran, kami tidak mengurangi unsur-unsur yang berpatroli,” ujar Ali di sela-sela acara pemberian paket bazar murah di Mabesal, Cilangkap, Jakarta Timur, Jumat (14/4/2023). “Mereka tetap bersiaga sebagaimana biasanya di beberapa tempat untuk menghindari kegiatan penyelundupan yang marak. Kami siagakan mereka baik di darat maupun di laut,” katanya lagi.</p>
    <p class="text-start"> Ali mengungkapkan, tidak ada libur atau cuti bagi prajurit Kapal Perang Republik Indonesia (KRI) selama libur lebaran Idul Fitri. “Jadi tidak ada istirahat atau cuti bagi anggota yang berdinas di KRI, khususnya yang jaga. Kapal-kapal jaga yang patroli itu tetap dilaksanakan walaupun hari lebaran,” ujar Ali.</p>
    <p class="text-start"> Terbaru, TNI AL melalui Pangkalan Udara TNI Angkatan Laut (Lanudal) Juanda Surabaya menggagalkan penyelundupan hewan endemik langka yang dilindungi, yakni kura-kura bercangkang lunak spesies labi-labi moncong babi dari Bandara Juanda tujuan Vietnam, pada Senin (10/4/2023). Pengungkapan kasus ini berawal dari kecurigaan petugas dengan beberapa koper yang melewati pemeriksaan X-Tray.</p>
    <p class="text-start"> “Total ada delapan koper yang dibawa tiga terduga pelaku dengan tujuan Surabaya-Singapura-Vietnam,” kata Komandan Lanudal Juanda Kolonel Laut (P) Heru Prasetyo, dikutip dari keterangan resmi TNI AL, Selasa (11/4/2023). Salah satu terduga pelaku berhasil diamankan dengan barang bukti labi-labi moncong babi sebanyak 5.632 ekor yang dimasukkan ke dalam koper.</p>
    <p class="text-start"> Selain itu, Lanudal Juanda sebagai leading sector dan koordinator pengamanan di Bandara Juanda juga menyita satu paspor atas nama David Setiawan, tiket rute Surabaya-Singapura, tiket rute Singapura-Vietnam, dua unit ponsel, uang tunai Rp 3.800.000 dan 9.000 dong Vietnam.</p>
    <p class="text-start"> “Adapun dengan delapan koper yang berisi labi-labi ini 5.632 ekor, kisaran harga Rp 150.000 per ekor, sehingga ditaksir negara mengalami kerugian sebesar Rp 844.800.000,” ujar Heru.</p>
