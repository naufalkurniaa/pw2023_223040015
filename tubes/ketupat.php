    <img src="img/gambar15.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Merebus ketupat membutuhkan waktu yang cukup lama agar beras matang merata. Banyak orang mengakali durasi waktu rebus ketupat dengan memasak dalam jumlah banyak sekaligus atau beli ketupat jadi.  Foodplace berbagi cara merebus ketupat hemat gas yaitu menggunakan rice cooker. Hasilnya beras matang sempurna dan sudah diuji coba di dapur Foodplace.</p>
    <p class="text-start"> Namun, sebaiknya pantau terus jumlah air pada rice cooker kamu, karena perbedaan merek dapat berpengaruh pada durasi masak.  Simak cara membuat ketupat rice cooker berikut. </p>
    <h5>bahan</h5>
    <ul>
        <li>300 gram beras</li>
        <li>3 liter air</li>
        <li>4 buah janur ketupat</li>
    </ul>
    <h5>Cara membuat ketupat rice cooker</h5>
    <p class="text-start">1. Siapkan janur ketupat, buka satu helai janur untuk mengisi beras.</p>
    <p class="text-start">2. Isi beras ke ketupat sekitar seperempat ruang janur ketupat. Tutup lagi janurnya.</p>
    <p class="text-start">3. Masukkan tiga liter air ke dalam baskom rice cooker, masukkan ketupat yang sudah diisi beras. Pilih menu masak bubur jika menggunakan rice cooker digital. Sementara rice cooker manual bisa dimasak seperti masak nasi biasa. Jika tombol sudah naik, kembali nyalakan.</p>
    <p class="text-start">4. Cek ketupat jika sudah keras, tiriskan dari air rebusan. Diamkan sejenak agar hasil nasi lebih kenyal.</p>

   
