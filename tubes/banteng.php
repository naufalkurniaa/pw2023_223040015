    <h1 class="text-start mb-3">Dikejar Banteng hingga Adu Jotos, Berikut 5 Festival Paling Berbahaya di Dunia</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023 15:40 WIB</p>
    <img src="img/gambar5.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start">BANDUNG, MenitNews - Festival adalah hari atau pekan gembira dalam rangka peringatan sebuah peristiwa penting dan bersejarah. Umumnya, sebuah festival dirayakan dengan iring-iringan, tarian, lomba, musik, dan hal-hal lain yang membuat semua orang bergembira. Namun, beberapa festival di dunia justru ada yang mengharuskan para pelakunya membahayakan diri mereka sendiri. Meski begitu, mereka tetap merasa senang dan bersemangat mengikuti festival tersebut. Biasanya karena festival yang diselenggarakan memiliki makna budaya maupun agama yang telah menjadi tradisi selama bertahun-tahun.</p>
    <h5>1. Festival Onbashira, Jepang</h5>
    <p class="text-start">Setiap enam tahun sekali, di wilayah Danau Suwa, Prefektur Nagano, Jepang, diadakan festival Onbashira. Ini adalah festival paling berbahaya di dunia. Dilansir dari Britannica, festival ini bertujuan untuk mengganti 16 tiang kayu di masing-masing sudut empat bangunan Kuil Agung Suwa. Perayaan dimulai di pegunungan dengan memilih 16 pohon cemara terbaik yang ditebang menggunakan alat penebang kayu tradisional. Kayu tersebut kemudian dibawa ke tempat tujuan dengan jarak sekitar 10 kilometer, dan bagian paling berbahaya adalah ketika batang kayu harus diluncurkan dari atas gunung. Sebab, para pria menunggangi batang kayu yang meluncur menuruni lereng gunung tersebut. Tidak jarang mereka menderita cedera parah, bahkan beberapa diantaranya kehilangan nyawa.</p>
    <h5>2. Festival Lari Banteng, Spanyol</h5>
    <p class="text-start">Festival paling berbahaya di dunia berikutnya adalah Fiesta de San Fermín yang diadakan pada bulan Juli di Pamplona, Spanyol. Setiap pagi di hari festival, sekitar 2.000 orang pemberani berbaris di titik awal jalur lari sepanjang 875 meter yang melalui jalan-jalan di pusat kota. Kegembiraan dimulai pada pukul 8:00 pagi, ketika orang-orang yang dikejar oleh banteng. Festival ini juga dikenal dengan nama encierro atau lari banteng. Tidak jarang beberapa peserta mengalami cedera karena terinjak atau diseruduk banteng. Menariknya, lebih dari separuh peserta yang mengikuti festival Lari Banteng adalah turis.</p>
    <h5>3. Festival Perang Jeruk, Italia</h5>
    <p class="text-start">Setiap tahun di bulan Februari, sebuah kota di Italia, Ivrea, mengadakan festival Battaglia delle Arance atau Perang Jeruk. Festival ini diadakan sebagai simbol pemberontakan abad pertengahan saat kota itu menggulingkan penguasa diktator. Sebuah gerobak yang ditarik kuda membawa jeruk, kemudian pemain yang mewakili pihak tiran masuk ke alun-alun. Mereka dikerumuni oleh gerombolan warga kota yang bertugas melempar jeruk. Meski festival tersebut terlihat seperti kekacauan, tetapi ada aturan penting di mana mereka tidak boleh melempar jeruk ke arah kuda.</p>
    <h5>4. Yanshui Beehive Rocket Festival, Taiwan</h5>
    <p class="text-start">Dilansir dari laman Holidappy, di Yanshui, Taiwan, memiliki pertunjukan piroteknik yang kerap menyebabkan banyak penonton yang cedera. Salah satu festival paling berbahaya di dunia ini dikenal dengan nama Yanshui Beehive Rocket Festival. Festival ini dimulai pada tahun 1880-an sebagai simbol seruan kepada para dewa untuk menyelamatkan masyarakat dari wabah kolera yang sedang merajalela. Menara ditempatkan di sekitar kota, kemudian diisi dengan berbagai macam petasan kembang api, membuatnya terlihat seperti sarang lebah. Namun, petasan tersebut tidak diluncurkan ke langit, melainkan ditembakkan secara horizontal ke arah orang-orang yang hadir. Peserta yang datang ke festival tersebut biasanya melengkapi diri mereka dengan pelindung seperti helm, visor, pakaian tebal, dan sarung tangan. Meski demikian, beberapa pengunjung tetap mengalami luka bakar, laserasi, hingga luka parah.</p>
    <h5>5. Festival Pertarungan Natal Takanakuy, Peru</h5>
    <p class="text-start">Di sebuah daerah tinggi di Pegunungan Andes, di wilayah Cusco, orang-orang merayakan Natal dengan cara yang unik. Tidak hanya dengan musik yang meriah, menari, atau makan dan minum, namun ada juga adu tinju. Tradisi ini disebut Takanakuy. Takanakuy adalah kata Quechua yang berarti “saat darah mendidih”, dan festival ini adalah simbol dari kesempatan untuk meluapkannya. Siapa saja, mulai dari pria, wanita, dan anak-anak dapat memasuki arena dan saling melepaskan pukulan di depan kerumunan yang berteriak kegirangan. Meski menyebabkan memar pada peserta acara, setiap pertarungan selalu diakhiri dengan jabat tangan atau pelukan.</p>
