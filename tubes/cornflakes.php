    <h1 class="text-start mb-3">Resep Cornflakes Cookies, Kue Kering Renyah Cepat Bikinnya</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023</p>
    <img src="img/gambar21.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Cornflakes merupakan jenis sereal yang terbuat dari jagung dan biasa disantap dengan susu cair saat sarapan. Selain disantap dengan susu cair, cornflakes bisa juga diolah menjadi kue kering. Kamu bisa sajikan kreasi cornflakes ini sebagai camilan saat momen Lebaran </p>
    <p class="text-start"> Pada resep ini bisa menghasilkan 700 gram cornflakes cookies. Kamu juga bisa jadikan resep ini sebagai ide jualan kue kering. Ikuti resep cornflakes cookies berikut ini, dikutip dari buku “Kue Kering Ekonomis” (2015) karya Ambarini terbitan PT Gramedia Pustaka Utama. </p>
    <h4>Resep cornflakes cookies</h4>
    <h5>Bahan</h5>
    <ul>
        <li>200 gram margarin</li>
        <li>3 sdm susu bubuk</li>
        <li>160 gram gula pasir halus</li>
        <li>1 butir kuning telur</li>
        <li>Setengah putih telur</li>
        <li>Setengah sdt vanili</li>
        <li>200 gram tepung terigu protein rendah</li>
        <li>100 gram cornflakes</li>
    </ul>
    <h5>Cara membuat cornflakes cookies</h5>
    <p class="text-start">1. Campurkan margarin, susu bubuk, gula pasir, kuning telur, putih telur, dan vanili. Kocok semua bahan sampai lembut. Tambahkan tepung terigu dan aduk rata. Masukkan cornflakes dan aduk lagi sampai semua bahan tercampur rata.</p>
    <p class="text-start">2. Siapkan loyang yang sudah diolesi dengan margarin. Ambil dan cetak adonan menggunakan dua buah sendok teh. Setelah dicetak, pipihkan adonan. Lakukan step ini sampai adonan habis.</p>
    <p class="text-start">3. Panggang adonan dengan oven bersuhu 120 derajat celcius selama 20 menit sampai berubah warna menjadi kuning kecokelatan. Setelah matang, angkat dari loyang dan dinginkan.</p>
    <p class="text-start">4. Simpan cookies di dalam wadah kedap udara.</p>
