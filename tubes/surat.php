    <h1 class="text-start mb-3">Beredar Surat Larangan ASN Kemenkes Berbicara RUU Kesehatan di Luar Forum Resmi, Apa Isinya?</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023</p>
    <img src="img/gambar14.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Sebuah surat yang ditandatangani oleh Direktur Jenderal Pelayanan Kesehatan (Yankes) Kementerian Kesehatan (Kemenkes) Azhar Jaya beredar di media sosial. Surat bertanggal 11 April 2023 itu ditujukan untuk pimpinan satuan kerja di kantor pusat Ditjen Yankes dan pimpinan Unit Pelaksana Teknis Ditjen Yankes.</p>
    <p class="text-start"> Salah satu poin dari surat tersebut adalah melarang ASN Kemenkes untuk membicarakan Rancangan Undang-Undang (RUU) Kesehatan di forum resmi. "Menkes melarang ASN kemenkes bahas soal UU kesehatan di luar forum resmi," tulis akun ini dalam unggahannya. Lantas, apa isinya dan bagaimana penjelasan Kemenkes?</p>
    <h5>Penjelasan Kemenkes</h5>
    <p class="text-start">Ketua Tim Informasi dan Hubungan Masyarakat Ditjen Yankes Haidar Istiqlal membenarkan adanya surat tersebut. "Benar, itu dari Ditjen Yankes yang ditujukan untuk Unit Pelaksana Teknis di lingkungan Ditjen Yankes, sebagaimana yang ada dalam isi surat," kata Haidar saat dikonfirmasi Kompas.com, Jumat (14/4/2023).</p>
    <p class="text-start">Surat tersebut memiliki lima poin imbauan kepada Aparatur Sipil Negara (ASN) Kemenkes dan pegawai badan layanan umum (BLU) terkait dengan adanya penyusunan Rancangan Undang-Undang (RUU) Kesehatan.</p>
    <h5>5 poin isi surat</h5>
    <p class="text-start">Pertama, ASN Kemenkes pada kantor pusat dan UPT serta pegawai BLU pada UPT Ditjen Yankes untuk mendukung dan berpartisipasi dalam proses sosialisasi positif RUU Kesehatan. Kedua, ASN Kemenkes tidak diperkenankan membahas RUU Kesehatan di luar forum resmi atau ikut menandatangani atau memberi saran melalui institusi atau organisasi di luar Kemenkes. Pasalnya, hal tersebut rawan disalahgunakan oleh organisasi atau institusi lain sehingga seolah-olah berseberangan sikap dengan Kemenkes.</p>
    <p class="text-start">Ketiga, pimpinan satuan kerja atau UPT serta ASN dan pegawai BLU di lingkungan Yankes wajib mematuhi hal-hal tersebut. Keempat, pimpinan satuan kerja atau UPT wajib mengawaji seluruh ASN atau pegawai BLU di lingkungan kerjanya. Selain itu, mereka juga harus mendukung sikap Kemenkes sebagai bagian yang tidak terpisahkan dari kementerian. Kelima, ketidakpatuhan terhadap hal-hal di atas akan dilakukan pembinaan secara administrasi sesuai dengan ketentuan perundang-undangan.</p>
    <h5>RUU Kesehatan masih dalam tahap pembahasan</h5>
    <p class="text-start">Hingga saat ini RUU Kesehatan masih dalam tahap pembahasan. Awal tahun ini, Ikatan Dokter Indonesia (IDI) secara tegas menolak RUU tersebut dan meminta agar dicabut dari Program Legislasi Nasional (Prolegnas) 2023. Salah satu alasan penolakan IDI adalah dicabutnya sejumlah Undang-Undang keprofesian.</p>
    <p class="text-start">"Ada sebagian Undang-Undang yang dicabut, khususnya Undang-Undang profesi, praktek kedokteran, perawat, bidan dan lain-lainnya," kata Wakil Ketua Umum PB IDI Slamet Budiarto, dikutip dari pemberitaan Kompas.com (16/1/2023). "Sehingga, praktis sudah tidak ada Undang-Undang keprofesian, ini yang kami menolak hal tersebut," ujarnya lagi.</p>
    <p class="text-start">Selain dicabutnya Undang-Undang keprofesian, draft RUU Kesehatan juga menyebutkan penentuan kompetensi dokter dilakukan oleh Kemenkes dan pemerintah daerah.</p>
