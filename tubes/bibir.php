    <h1 class="text-start mb-3">11 Penyebab Bibir Kering Dan Mengelupas Yang Perlu Diketahui</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023</p>
    <img src="img/gambar17.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Bibir yang kering dan mengelupas akan menyebabkan rasa tidak nyaman dan terkadang terasa sangat perih ketika mengonsumsi makanan tertentu, seperti makanan yang asam dan pedas.</p>
    <p class="text-start"> Kondisi ini bisa disebabkan oleh berbagai faktor, mulai dari paparan udara yang panas atau dingin hingga mengalami dehidrasi. Untuk lebih jelasnya, ketahui penyebab bibir kering dan mengelupas berikut ini.</p>
    <h5>Penyebab bibir kering dan mengelupas</h5>
    <p class="text-start">Dilansir dari Cleveland Clinic, ada beberapa penyebab bibir kering dan mengelupas yang akan dialami, seperti:</p>
    <ul>
        <li>Mengalami iritasi karena terpapar sinar matahari.</li>
        <li>Dapatkan paparan udara dingin.</li>
        <li>Berada pada cuaca yang panas atau kering.</li>
        <li>Memiliki kebiasaan menjilati bibir.</li>
        <li>Mengalami reaksi terhadap obat atau memiliki riwayat gangguan kesehatan tertentu, seperti alergi, gangguan tiroid, atau gangguan sistem imun tubuh.</li>
        <li>Kekurangan vitamin, seperti vitamin B dan zat besi.</li>
        <li>Mengalami dehidrasi.</li>
    </ul>
    <p class="text-start"> Selain beberapa kondisi tersebut, bibir yang kering dan mengelupas bisa disebabkan oleh infeksi. Menurut Verywell Health, ada beberapa jenis organisme yang memicu infeksi dan menyebabkan bibir mengelupas, seperti:</p>
    <ul>
        <li>Virus, seperti herpes simpleks atau human immunodeficiency virus (HIV).</li>
        <li>Bakteri, seperti streptococcus dan staphylococcus.</li>
        <li>Jamur, seperti Candida.</li>
        <li>Parasit, seperti Leishmania yang bisa menyebar melalui gigitan serangga.</li>
    </ul>
    <p class="text-start">Bibir yang kering dan terkelupas umumnya tidak disebabkan oleh kondisi yang parah dan bisa disembuhkan tanpa perawatan medis tertentu.</p>
    <h5>Kapan harus ke dokter</h5>
    <p class="text-start">Dilansir dari Cleveland Clinic, bibir kering dan terkelupas umumnya bisa membaik dengan mencukupi kebutuhan cairan tubuh dengan banyak minum air dan mengaplikasikan lip balm atau pelembap bibir. Namun, ketika cara-cara tersebut sudah dilakukan dan kondisi bibir tidak kunjung membaik dalam beberapa minggu, Anda disarankan untuk mencari bantuan medis.</p>
    <p class="text-start">Dokter akan melakukan tes khusus untuk memeriksa apakah bibir yang kering dan mengelupas tersebut merupakan gejala dari penyakit tertentu atau tidak. Dengan mengetahui penyebab bibir kering dan mengelupas tersebut, Anda bisa melakukan tindakan yang diperlukan untuk mencegah dan mengatasinya.</p>
    <p class="text-start">Meskipun umumnya bukan merupakan kondisi yang serius, Anda tetap diimbau untuk tidak melakukan diagnosis pribadi dan mencari bantuan medis ketika bibir tetap kering dan mengelupas setelah beberapa minggu.</p>
