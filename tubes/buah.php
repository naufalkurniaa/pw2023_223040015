    <p class="text-start"> BANDUNG, MenitNews - Selain minum air setidaknya delapan gelas per hari, konsumsi buah selama puasa juga disarankan untuk mencukupi kebutuhan cairan tubuh. Mengonsumsi buah yang mengandung banyak air saat sahur dan berbuka, seperti melon, semangka, pepaya, hingga apel, sangat disarankan. Untuk lebih jelasnya, ketahui buah yang bagus untuk sahur dan buka puasa berikut ini.</p>
    <h5>Buah yang bagus untuk sahur dan buka puasa</h5>
    <p class="text-start">Dikutip dari Antara News, Selasa (11/04/2023), ahli gizi serta dosen Fakultas Ilmu Kesehatan Universitas Esa Unggul Nazhif Gifari, S.Gz, M.Si menjelaskan, mengonsumsi buah-buahan yang mengandung air saat puasa adalah salah satu cara yang bisa dilakukan untuk memenuhi kebutuhan cairan tubuh.</p>
    <p class="text-start">Buah-buahan yang memiliki kadar air tinggi yang disarankan saat sahur dan berbuka puasa, yakni melon, semangka, pepaya, apel, dan pir. Dilansir dari Healthline, buah-buahan tersebut memiliki kadar air lebih dari 90 persen sehingga sangat baik untuk menjaga tubuh dari dehidrasi.</p>
    <p class="text-start">Menyimpan buah-buahan tersebut di dalam kulkas sebelum dikonsumsi saat buka puasa juga disarankan untuk mengatasi dehidrasi yang disebabkan oleh udara yang panas selama seharian berpuasa. Dengan mengonsumsinya, tubuh bisa menyerap air dengan lebih baik, khususnya jika perut terasa tidak nyaman.</p>
    <p class="text-start">Selain memiliki kadar air yang tinggi, Nazhif juga menyebabutkan bahwa mengonsumsi buah-buahan tersebut selama puasa juga akan sangat baik untuk kesehatan karena mengandung banyak vitamin dan mineral, seperti vitamin A, B, dan C, serta kalsium, magnesium, dan kalium.</p>
    <h5>Waktu yang disarankan untuk makan buah selama puasa</h5>
    <p class="text-start">Nazhif menjelaskan bahwa waktu yang disarankan untuk mengonsumsi buah-buahan tersebut adalah beberapa saat sebelum imsak sehingga tidak akan cepat haus saat puasa. Berbuka puasa dengan buah terlebih dahulu juga disarankan untuk membantu menetralkan asam lambung serta mencukupi kebutuhan gula alami tubuh yang hilang selama berpuasa.</p>
    <p class="text-start">Selain buah-buahan tersebut, mengonsumsi kurma juga disarankan ketika berbuka puasa karena memiliki kandungan gula alami yang diperlukan oleh tubuh setelah seharian berpuasa. Meskipun baik untuk tubuh, ada beberapa jenis buah yang perlu dihindari atau dibatasi konsumsinya selama puasa, seperti nangka dan durian.</p>
    <p class="text-start">Buah-buahan tersebut mengandung gas sehingga akan membuat perut tidak terasa nyaman ketika dikonsumsi saat puasa. Meskipun bisa membantu tubuh untuk mencukupi kebutuhan cairan per hari, mengonsumsi buah saja tidak cukup. Nazhif menyebutkan bahwa konsumsi air dalam sehari selama puasa tetap harus sama, yakni setidaknya delapan gelas per hari.</p>
    <p class="text-start">Setelah mengetahui buah yang bagus untuk sahur dan buka puasa tersebut, Anda bisa mulai menyediakannya sebagai menu tambahan selama berpuasa. Namun, batasi konsumsinya agar tidak terlalu berlebihan dan tetap penuhi kebutuhan cairan tubuh dengan minum air putih setidaknya delapan gelas per hari.</p>

