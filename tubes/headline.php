    <p class="text-start"> BANDUNG, MenitNews - Menteri Perhubungan (Menhub) Budi Karya Sumadi menyarankan masyarakat yang hendak melakukan perjalanan mudik Lebaran 2023 dan sudah cuti bisa mulai berangkat lebih awal pada akhir pekan ini, yaitu 13-17 April 2023. Hal tersebut karena jalanan belum terlalu padat apabila dibandingkan ketika masuk periode puncak Lebaran , yakni 19-21 April 2023. "Bagi masyarakat yang sudah bisa libur, silakan mudik lebih awal mulai hari ini, tanggal 13-17 April nanti, karena jalanan masih tidak terlalu padat," ujar Budi dalam keterangannya, Kamis (13/4/2023).</p>
    <p class="text-start"> Menurut dia, pemerintah akan melakukan sejumlah upaya untuk memecah kepadatan arus lalu lintas di satu hari tertentu. Hal ini dilakukan agar tidak terjadi satu puncak arus mudik Lebaran 2023 yang terlalu tinggi. "Kami berharap dengan upaya antisipasi yang dilakukan, perbandingan antara kapasitas jalan maupun simpul transportasi dengan volume penumpang dan kendaraan atau volume to capacity (V/C) ratio bisa tetap di bawah angka 1, yang artinya masih lancar," ujar Budi. Sebagai antisipasi kepadatan arus kendaraan di puncak arus mudik Lebaran 2023, Kemenhub menyiapkan berbagai skenario rekayasa lalu lintas, seperti contraflow, one way, ganjil genap, dan pembatasan angkutan barang.</p>
    <p class="text-start"> Kemenhub memperkirakan jumlah pemudik pada tahun ini mencapai 123,8 juta orang atau 45,8 persen dari jumlah penduduk Indonesia. Angka tersebut lebih banyak 46 persen dari jumlah pemudik tahun 2022 sebanyak 85 juta orang.</p>
   
