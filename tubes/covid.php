    <h1 class="text-start mb-3">Kasus Covid-19 Naik Lagi, Apakah Disebabkan Subvarian Baru Arcturus?</h1>
    <p class="text-start mb-0">MenitNews - 14/04/2023 15:40 WIB</p>
    <img src="img/gambar13.jpg" class="img-fluid my-3" width="800" height="400" alt="Gambar Berita">
    <p class="text-start">Penulis: Naufal Kurnia</p>
    <p class="text-start"> BANDUNG, MenitNews - Kementerian Kesehatan (Kemenkes) mengumumkan bahwa subvarian Arcturus atau Covid-19 subvarian Omicron XBB.1.16 telah terdeteksi di Indonesia. Pengumuman ini disampaikan saat Kasus Covid-19 di Indonesia mengalami tren kenaikan dalam beberapa hari terakhir.</p>
    <p class="text-start">Laporan Kemenkes RI pada Kamis (13/4/2023) pukul 12.00 WIB kasus Covid-19 bertambah 990 dalam sehari. Sementara itu, hari sebelumnya yakni pada Rabu (12/4/2023) pukul 12.00 WIB, kasus harian bertambah sebanyak 987 kasus.</p>
    <p class="text-start">DKI Jakarta menjadi wilayah dengan penambahan kasus konfirmasi paling banyak yakni per 13 April 2023, penambahan kasus adalah sebanyak 441 kasus. Lantas apa yang menjadi penyebab naiknya kasus Covid-19 di Indonesia, apakah berhubungan dengan terdeteksinya subvarian Arcturus?</p>
    <h4>Penjelasan Kemenkes</h4>
    <p class="text-start">Kepala Biro Komunikasi dan Pelayanan Publik Kemenkes, Siti Nadia Tarmizi mengatakan, pihaknya saat ini tengah mengamati kemungkinan yang menjadi penyebab kenaikan kasus Covid-19 di Indonesia. Nadia mengatakan, subvarian Arcturus memang sudah ditemukan di Indonesia, tetapi menurutnya subvarian yang saat ini mendominasi masih merupakan varian BA.4.</p>
    <p class="text-start">"Dari hasil genomik subvarian yang mendominasi masih BA.4 yang sifatnya juga cepat menular," ungkap Nadia dihubungi Kompas.com, Jumat (14/4/2023). Menurutnya, penyebab kenaikan kasus bisa juga karena saat ini masyarakat mulai mengendurkan terhadap protokol kesehatan. </p>
    <p class="text-start">"Sudah tidak tes kalau batuk atau pilek dan tidak isoman, sehingga memberikan potensi penularan ke orang lain," ujarnya. Guna mencegah Covid-19 kembali melonjak, maka masyarakat diimbau untuk melakukan vaksin booster atau vaksin ketiga. Selain itu pihaknya juga mengingatkan agar masyarakat tetap mengenakan masker jika sakit dan segera tes jika memiliki gejala sakit.</p>
    <h4>Penemuan kasus subvarian Arcturus</h4>
    <p class="text-start">Subvarian Arcturus sudah ditemukan pada tanggal 23 dan tanggal 27 Maret 2023 dari dua orang yang berbeda. Satu kasus merupakan Pelaku Perjalanan Luar Negeri (PPLN). Gejala yang dialami pasien menurutnya tidak berat. Subvarian ini memang cepat menular, namun menurutnya tak menimbulkan fatalitas.</p>
    <p class="text-start">"Hanya satu sempat dirawat, yang satu tidak ada gejala dan pelaku perjalanan LN," ucap Nadia, dikutip dari Kompas.com (14/4/2023). Pasien tersebut sudah sembuh dan hanya sakit sekitar 5-6 hari.</p>
