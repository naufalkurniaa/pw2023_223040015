    <p class="text-start">BANDUNG, MenitNews - Tubuh membutuhkan seng, tetapi jika berlebihan dapat menyebabkan efek negatif untuk kesehatan.</p>
    <p class="text-start">Mengutip Ro Health Guide, seng atau zinc adalah mineral esensial yaang penting untuk perkembangan sel, kesehatan sistem kekebalan tubuh, metabolisme, dan banyak lagi.</p>
    <p class="text-start">Mikronutrien ini bisa ditemukan dalam makanan, seperti tiram, daging merah, dan kacang-kacangan. Sumber lainnya adalah suplemen zinc. Suplemen zinc inilah yang biasanya dapat menyebabkan efek buruk untuk kesehatan, jika dikonsumsi berlebihan.</p>
    <p class="text-start">Kondisi tubuh kelebihan seng disebut sebagai toksisitas seng atau keracunan seng. Kebanyakan kasus overdosis seng tidak sampai mengancam jiwa, tetapi komplikasi kesehatan jangka panjang dapat terjadi sebagai akibatnya. Komplikasi tubuh kelebihan zinc ini contohnya, anemia, mati rasa atau kelemahan saraf, masalah bergerak, dan kejang otot. Berikut artikel ini akan menunjukkan sejumlah tanda-tanda tubuh kelebihan seng agar Anda mewaspadainya.</p>
    <h5>Apa saja tanda-tanda tubuh kelebihan seng?</h5>
    <p class="text-start">Mengutip GoodRx Health, konsumsi zinc ada batas maksimal konsumsinya berdasarkan usia, sebagai berikut:</p>
    <ul>
        <li>Usia 0-6 bulan: maksimal 4 mg per hari.</li>
        <li>Usia 7-12 bulan: maksimal 5 mg per hari.</li>
        <li>Usia 1-3 tahun: maksimal 7 mg per hari.</li>
        <li>Usia 4-8 tahun: maksimal 12 mg per hari.</li>
        <li>Usia 9-13 tahun: maksimal 23 mg per hari.</li>
        <li>Usia 14-18 tahun: maksimal 34 mg per hari.</li>
        <li>Usia 19 tahun ke atas: maksimal 40 mg per hari.</li>
    </ul>
    <p class="text-start">Jika Anda mengkonsumsi seng melebihan batas aman, itu yang bisa menyebabkan munculnya tanda-tanda tubuh kelebihan zinc.</p>
    <p class="text-start">Toksisitas seng sebenarnya jarang terjadi. Namun, bukan berarti tidak mungkin ini terjadi pada Anda. Sehingga, kita perlu tetap mewaspadai tanda-tanda tubuh kelebihan seng, yang meliputi berikut:</p>
    <ul>
        <li>Diare</li>
        <li>Sakit perut</li>
        <li>Mual</li>
        <li>Muntah</li>
        <li>Kehilangan selera makan</li>
        <li>Sakit kepala</li>
    </ul>
    <p class="text-start">Meski tanda-tanda seperti di atas muncul, tubuh kelebihan seng sulit diidentifikasi karena gejalanya mirip dengan efek samping obat dan kondisi kesehatan lain. Jika Anda khawatir gejala tubuh kelebihan seng terjadi pada Anda, lebih baik segera periksa ke dokter.</p>


